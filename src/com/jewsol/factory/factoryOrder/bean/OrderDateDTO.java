package com.jewsol.factory.factoryOrder.bean;

public class OrderDateDTO {
	private int orderDateSeq;
	private String orderDate;
	private String orderClose;
	public int getOrderDateSeq() {
		return orderDateSeq;
	}
	public void setOrderDateSeq(int orderDateSeq) {
		this.orderDateSeq = orderDateSeq;
	}
	public String getOrderDate() {
		return orderDate;
	}
	public void setOrderDate(String orderDate) {
		this.orderDate = orderDate;
	}
	public String getOrderClose() {
		return orderClose;
	}
	public void setOrderClose(String orderClose) {
		this.orderClose = orderClose;
	}
	
	
}

package com.jewsol.store.option.bean;

public class OptionDTO {
	private int optionSeq;
	private int storeProductSeq;
	private String optionName;
	private int optionNumber;
	private int optionLabor;
	private int optionPrice;
	public final int getOptionSeq() {
		return optionSeq;
	}
	public final void setOptionSeq(int optionSeq) {
		this.optionSeq = optionSeq;
	}
	public final int getStoreProductSeq() {
		return storeProductSeq;
	}
	public final void setStoreProductSeq(int storeProductSeq) {
		this.storeProductSeq = storeProductSeq;
	}
	public final String getOptionName() {
		return optionName;
	}
	public final void setOptionName(String optionName) {
		this.optionName = optionName;
	}

	public final int getOptionLabor() {
		return optionLabor;
	}
	public final void setOptionLabor(int optionLabor) {
		this.optionLabor = optionLabor;
	}
	public final int getOptionPrice() {
		return optionPrice;
	}
	public final void setOptionPrice(int optionPrice) {
		this.optionPrice = optionPrice;
	}
	public final int getOptionNumber() {
		return optionNumber;
	}
	public final void setOptionNumber(int optionNumber) {
		this.optionNumber = optionNumber;
	}
	
	
	
	
}

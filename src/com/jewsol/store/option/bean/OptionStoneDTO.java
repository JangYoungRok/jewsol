package com.jewsol.store.option.bean;

public class OptionStoneDTO {
	private int optionStoneSeq;
	private int optionSeq;
	private int stoneSeq;
	private String stoneName;
	private String stoneSize;
	private int optionStoneQty;
	private int optionStonePrice;
	private int optionStoneLabor;
	
	public final String getStoneSize() {
		return stoneSize;
	}
	public final void setStoneSize(String stoneSize) {
		this.stoneSize = stoneSize;
	}
	public final int getOptionStoneSeq() {
		return optionStoneSeq;
	}
	public final void setOptionStoneSeq(int optionStoneSeq) {
		this.optionStoneSeq = optionStoneSeq;
	}
	public final int getOptionSeq() {
		return optionSeq;
	}
	public final void setOptionSeq(int optionSeq) {
		this.optionSeq = optionSeq;
	}
	public final int getStoneSeq() {
		return stoneSeq;
	}
	public final void setStoneSeq(int stoneSeq) {
		this.stoneSeq = stoneSeq;
	}
	public final String getStoneName() {
		return stoneName;
	}
	public final void setStoneName(String stoneName) {
		this.stoneName = stoneName;
	}
	public final int getOptionStoneQty() {
		return optionStoneQty;
	}
	public final void setOptionStoneQty(int optionStoneQty) {
		this.optionStoneQty = optionStoneQty;
	}
	public final int getOptionStonePrice() {
		return optionStonePrice;
	}
	public final void setOptionStonePrice(int optionStonePrice) {
		this.optionStonePrice = optionStonePrice;
	}
	public final int getOptionStoneLabor() {
		return optionStoneLabor;
	}
	public final void setOptionStoneLabor(int optionStoneLabor) {
		this.optionStoneLabor = optionStoneLabor;
	}
	
	
}

package com.jewsol.store.stone.bean;

public class StoneSizeDTO {

	private int stoneSizeSeq;
	private String stoneSize;
	private float stoneSizeValue;
	private int stoneLabor;
	public int getStoneSizeSeq() {
		return stoneSizeSeq;
	}
	public void setStoneSizeSeq(int stoneSizeSeq) {
		this.stoneSizeSeq = stoneSizeSeq;
	}
	public String getStoneSize() {
		return stoneSize;
	}
	public void setStoneSize(String stoneSize) {
		this.stoneSize = stoneSize;
	}
	public float getStoneSizeValue() {
		return stoneSizeValue;
	}
	public void setStoneSizeValue(float stoneSizeValue) {
		this.stoneSizeValue = stoneSizeValue;
	}
	public int getStoneLabor() {
		return stoneLabor;
	}
	public void setStoneLabor(int stoneLabor) {
		this.stoneLabor = stoneLabor;
	}
	
	
	
}

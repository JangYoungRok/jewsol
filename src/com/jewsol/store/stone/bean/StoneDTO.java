package com.jewsol.store.stone.bean;

public class StoneDTO {
	private int stoneSeq;
	private int branchSeq;
	private int stoneSizeSeq;
	private String stoneSize;
	private int stonePrice;
	private int stoneLabor;
	private int stoneCost;
	private String stoneName;
	private String stoneInUse;
	
	public int getBranchSeq() {
		return branchSeq;
	}
	public void setBranchSeq(int branchSeq) {
		this.branchSeq = branchSeq;
	}
	public int getStoneSeq() {
		return stoneSeq;
	}
	public void setStoneSeq(int stoneSeq) {
		this.stoneSeq = stoneSeq;
	}
	public int getStoneSizeSeq() {
		return stoneSizeSeq;
	}
	public void setStoneSizeSeq(int stoneSizeSeq) {
		this.stoneSizeSeq = stoneSizeSeq;
	}
	public String getStoneSize() {
		return stoneSize;
	}
	public void setStoneSize(String stoneSize) {
		this.stoneSize = stoneSize;
	}
	public int getStonePrice() {
		return stonePrice;
	}
	public void setStonePrice(int stonePrice) {
		this.stonePrice = stonePrice;
	}
	public int getStoneLabor() {
		return stoneLabor;
	}
	public void setStoneLabor(int stoneLabor) {
		this.stoneLabor = stoneLabor;
	}
	public int getStoneCost() {
		return stoneCost;
	}
	public void setStoneCost(int stoneCost) {
		this.stoneCost = stoneCost;
	}
	public String getStoneName() {
		return stoneName;
	}
	public void setStoneName(String stoneName) {
		this.stoneName = stoneName;
	}
	public String getStoneInUse() {
		return stoneInUse;
	}
	public void setStoneInUse(String stoneInUse) {
		this.stoneInUse = stoneInUse;
	}
	
	
	
	
}

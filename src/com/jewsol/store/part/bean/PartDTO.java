package com.jewsol.store.part.bean;

public class PartDTO {
	private int partSeq;
	private int branchSeq;
	private int partAttributeSeq;
	private String partName;
	private int partPrice;
	private String partAttribute;
	private int partCost;
	private int partLabor;
	
	public int getBranchSeq() {
		return branchSeq;
	}
	public void setBranchSeq(int branchSeq) {
		this.branchSeq = branchSeq;
	}
	public int getPartSeq() {
		return partSeq;
	}
	public void setPartSeq(int partSeq) {
		this.partSeq = partSeq;
	}
	public int getPartAttributeSeq() {
		return partAttributeSeq;
	}
	public void setPartAttributeSeq(int partAttributeSeq) {
		this.partAttributeSeq = partAttributeSeq;
	}
	public String getPartName() {
		return partName;
	}
	public void setPartName(String partName) {
		this.partName = partName;
	}
	public int getPartPrice() {
		return partPrice;
	}
	public void setPartPrice(int partPrice) {
		this.partPrice = partPrice;
	}
	public String getPartAttribute() {
		return partAttribute;
	}
	public void setPartAttribute(String partAttribute) {
		this.partAttribute = partAttribute;
	}
	public int getPartCost() {
		return partCost;
	}
	public void setPartCost(int partCost) {
		this.partCost = partCost;
	}
	public int getPartLabor() {
		return partLabor;
	}
	public void setPartLabor(int partLabor) {
		this.partLabor = partLabor;
	}
	
	

}

package com.jewsol.core.color.bean;

public class ColorDTO {
	private int colorSeq;
	private String colorName;
	private String colorType;
	public final int getColorSeq() {
		return colorSeq;
	}
	public final void setColorSeq(int colorSeq) {
		this.colorSeq = colorSeq;
	}
	public final String getColorName() {
		return colorName;
	}
	public final void setColorName(String colorName) {
		this.colorName = colorName;
	}
	public final String getColorType() {
		return colorType;
	}
	public final void setColorType(String colorType) {
		this.colorType = colorType;
	}
	
	
}

package com.jewsol.core.plate.bean;

public class PlateDTO {
	private int plateSeq;
	private String plateName;
	private int paltePrice;
	private int plateCost;
	public final int getPlateSeq() {
		return plateSeq;
	}
	public final void setPlateSeq(int plateSeq) {
		this.plateSeq = plateSeq;
	}
	public final String getPlateName() {
		return plateName;
	}
	public final void setPlateName(String plateName) {
		this.plateName = plateName;
	}
	public final int getPaltePrice() {
		return paltePrice;
	}
	public final void setPaltePrice(int paltePrice) {
		this.paltePrice = paltePrice;
	}
	public final int getPlateCost() {
		return plateCost;
	}
	public final void setPlateCost(int plateCost) {
		this.plateCost = plateCost;
	}
	
	
}
